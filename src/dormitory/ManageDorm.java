    private void loadDorms() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing table data

        try {
            // Database connection
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dormitory_db", "root", ""); 
            String sql = "SELECT dorm_id, dorm_name, owner_id, location FROM dorm"; // Added location to the SELECT query
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                int dormID = rs.getInt("dorm_id");
                String dormName = rs.getString("dorm_name");
                int ownerID = rs.getInt("owner_id");
                String location = rs.getString("location"); // Fetch location from the result set

                model.addRow(new Object[]{dormID, dormName, ownerID, location}); // Include location in the table data
            }

            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
