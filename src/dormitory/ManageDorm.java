    private void RemoveDormButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveDormButtonActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a dorm to remove.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Get Dorm ID from selected row
        int dormID = (int) jTable1.getValueAt(selectedRow, 0);

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this dorm?", "Confirm Deletion", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                // Database connection
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dormitory_db", "root", "");

                // SQL DELETE statement
                String sql = "DELETE FROM dorm WHERE dorm_id = ?";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setInt(1, dormID);

                int deletedRows = pst.executeUpdate();

                if (deletedRows > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Dorm removed successfully.");
                    loadDorms(); // Refresh the table after deletion
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Failed to remove dorm.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                }

                con.close();
            } catch (Exception e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_RemoveDormButtonActionPerformed

